$(function(){
//获得文本框对象
var t = $("#text_box");
//数量增加操作
$("#add").click(function(){
t.val(parseInt(t.val())+1);
if(parseInt(t.val())>1){
    $('#min').attr('disabled',false);
}
})
//数量减少操作
$("#min").click(function(){
       if(parseInt(t.val())==1){
           $('#min').attr('disabled',true);
       }
       if(parseInt(t.val())>1){
       t.val(parseInt(t.val())-1);
   }
})
})
